package gameModel;

public class Board {
	private int tile_size;
	private int width;
	private int height;

	public Board(int tilesize, int width, int height){
		this.height = height;
		this.tile_size = tilesize;
		this.width = width;
	}

	public int getTileSize(){
		return tile_size;
	}

	public int getWidth(){
		return width;
	}

	public int getHeight(){
		return height;
	}

}
